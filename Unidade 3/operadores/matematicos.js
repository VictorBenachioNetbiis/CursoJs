//Operadores matematicos

//adição

let soma = 5 + 2;
console.log(soma);

//subtração

let subtracao = 5 - 2;
console.log(subtracao);

//multiplicação
let multiplicacao = 2 * 1;
console.log(multiplicacao);

//divisao
let divisao = 5 / 2 ;
console.log(divisao);

//Resto da divisao
let resto = 5 % 2;
console.log(resto);

//exponenciação

let exponenciação = 5**2;
console.log(exponenciação);

//operadores incremento e decremento

let numero = 5;
//primeira forma de incremento
numero = numero + 1;
//segunda forma de incremento
numero += 1;
//teceira forma de incremento 
numero++;

//decremento primeira forma
numero = numero -1;
//segunda forma
numero -=1;
//terceira forma
numero--;

//Tipos de incremento e decremento que funcionam apenas com subtracação e adição
numero++;
numero--;

