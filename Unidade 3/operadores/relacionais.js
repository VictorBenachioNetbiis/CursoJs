//Operadores relacionais

//igual a
let igual = 5 == 5;
console.log("igual:", igual);//true ou false depende da comparação, neste caso é true

//Identico a (===)
let Identico = 5 === "5";
console.log("identico:",Identico); //além de verificar o valor, verifica o tipo //false

//diferente a 
let diferente = 5 != 5;
console.log("diferente:",diferente);//false

//diferente para validar o tipo tmbm
let diferenteRestrito = 5 !== 5;
console.log("diferente restrito:",diferenteRestrito); //true

//maior que (>)
let maiorQue = 5 > 3;
console.log("Maior que:", maiorQue);

//menor que (<)
let menosQue = 5 < 3;
console.log("Menor que:",menosQue);

//Maior ou igual  (>=)
let maiorIgual = 5 >= 3;
console.log("Maior ou igual:",maiorIgual);

//menor ou igual (<=)
let menorIgual = 5 <= 3;
console.log("menor ou igual:",menorIgual);