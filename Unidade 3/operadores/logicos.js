//operadores logicos

// E (&&)
// retorna verdadeiro se todos operandos forem verdadeiros
let e = true && true;


//OU (||)
//retorna verdadeiro se pelo menos um dos operadores for verdadeiro
let ou = true || false;

//negação (!)
// inverte o valor do operando
let negacao = !true;
let negacao2 = !false;

console.log(e);
console.log(ou);
console.log(negacao);
console.log(negacao2);
